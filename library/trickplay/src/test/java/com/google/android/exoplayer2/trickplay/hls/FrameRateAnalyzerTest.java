package com.google.android.exoplayer2.trickplay.hls;

import android.net.Uri;
import androidx.test.ext.junit.runners.AndroidJUnit4;

import java.io.ByteArrayInputStream;
import java.util.ArrayList;
import java.util.Arrays;
import java.util.Collections;
import java.util.List;

import org.junit.Before;
import org.junit.Test;
import org.junit.runner.RunWith;

import com.google.android.exoplayer2.C;
import com.google.android.exoplayer2.Format;
import com.google.android.exoplayer2.source.hls.playlist.DefaultHlsPlaylistParserFactory;
import com.google.android.exoplayer2.source.hls.playlist.HlsMasterPlaylist;
import com.google.android.exoplayer2.source.hls.playlist.HlsMediaPlaylist;
import com.google.android.exoplayer2.source.hls.playlist.HlsPlaylist;
import com.google.android.exoplayer2.source.hls.playlist.HlsPlaylistParserFactory;
import com.google.android.exoplayer2.upstream.ParsingLoadable;
import com.google.common.base.Charsets;
import static com.google.android.exoplayer2.trickplay.hls.HlsPlaylistUtils.createMockPlaylist;
import static com.google.android.exoplayer2.trickplay.hls.HlsPlaylistUtils.createMockSegment;
import static com.google.common.truth.Truth.assertThat;

@RunWith(AndroidJUnit4.class)
public class FrameRateAnalyzerTest {

  private static final String SRC_IFRAME_URI = "test_playlist.m3u8";
  public static final int[] TEST_SUBSETS = new int[] {2, 3};
  private HlsMasterPlaylist testMasterPlaylist;
  private ArrayList<HlsMediaPlaylist.Segment> test2SecondSegments;
  private ArrayList<HlsMediaPlaylist.Segment> test3SecondSegments;
  private Format baseFormat;
  private List<HlsMasterPlaylist.Variant> iframeOnlyVaraints = new ArrayList<>();
  private HlsMasterPlaylist.Variant sourceIFrameVariant = null;

  @Before
  public void setUp() throws Exception {
    baseFormat = new Format.Builder()
        .setRoleFlags(C.ROLE_FLAG_TRICK_PLAY)
        .setHeight(720)
        .setWidth(1280)
        .setPeakBitrate(1373400)
        .build();


    String testMaster = "#EXTM3U\n" +
        "#EXT-X-VERSION:5\n" +
        "#EXT-X-STREAM-INF:BANDWIDTH=2160000,CODECS=\"mp4a.40.2,avc1.64001f\",RESOLUTION=720x480\n" +
        "dummy.m3u8\n" +
        "#EXT-X-I-FRAME-STREAM-INF:BANDWIDTH=" + baseFormat.peakBitrate + ",URI=\"" + SRC_IFRAME_URI
        + "\",CODECS=\"avc1.640020\",RESOLUTION=" + baseFormat.width + "x" + baseFormat.height + "\n";


    HlsPlaylistParserFactory parserFactory =
        new DualModeHlsPlaylistParserFactory(new DefaultHlsPlaylistParserFactory(), TEST_SUBSETS);
    ParsingLoadable.Parser<HlsPlaylist> masterParser = parserFactory.createPlaylistParser();
    ByteArrayInputStream inputStream =
        new ByteArrayInputStream(testMaster.getBytes(Charsets.UTF_8));
    testMasterPlaylist = (HlsMasterPlaylist) masterParser.parse(Uri.EMPTY, inputStream);

    test2SecondSegments = new ArrayList<>();
    test3SecondSegments = new ArrayList<>();
    for (int i=0; i<10; i++) {
      test2SecondSegments.add(createMockSegment("", 0, 0, 2_000_000));
      test3SecondSegments.add(createMockSegment("", 0, 0, ((i % 2) == 0) ? 2_000_000 : 4_000_000));
    }


    // Assert properties of the variants generated by the DualModeHlsPlaylistParserFactory's AugmentedPlaylistParser
    // that are assumptions of these test cases
    //
    assertThat(testMasterPlaylist.variants.size()).isEqualTo(2 + TEST_SUBSETS.length);

    for (HlsMasterPlaylist.Variant variant : testMasterPlaylist.variants) {
      if ((variant.format.roleFlags & C.ROLE_FLAG_TRICK_PLAY) == C.ROLE_FLAG_TRICK_PLAY) {
        iframeOnlyVaraints.add(variant);
        if (variant.url.toString().equals(SRC_IFRAME_URI)) {
          sourceIFrameVariant = variant;
          assertThat(variant.format.label).isEqualTo(AugmentedPlaylistParser.SRC_FORMAT_LABEL);
        } else {
          assertThat(variant.format.frameRate).isNotEqualTo(Format.NO_VALUE);
        }
      }
    }
    assertThat(iframeOnlyVaraints.size()).isEqualTo(TEST_SUBSETS.length + 1);
    assertThat(sourceIFrameVariant).isNotNull();

  }

  @Test
  public void test_formatMatching() {

    Format firstFormat = new Format.Builder()
        .setRoleFlags(C.ROLE_FLAG_TRICK_PLAY)
        .setHeight(720)
        .setWidth(1280)
        .setPeakBitrate(1373400)
        .build();

    Format secondFormat = firstFormat.buildUpon()
        .setPeakBitrate(baseFormat.peakBitrate / 2)
        .setFrameRate(baseFormat.frameRate / 2)
        .build();

    FrameRateAnalyzer.FormatKey key = new FrameRateAnalyzer.FormatKey(firstFormat);

    assertThat(key).isNotEqualTo(new FrameRateAnalyzer.FormatKey(secondFormat));

    // FormatKey only considers the propegated Format.bitrate value, if it comes from avg or peak is not relevant
    Format avg = firstFormat.buildUpon()
        .setAverageBitrate(firstFormat.bitrate)
        .setPeakBitrate(Format.NO_VALUE)
        .build();

    assertThat(key).isEqualTo(new FrameRateAnalyzer.FormatKey(avg));


  }

  @Test
  public void test_analyzeFrameRate_noSegments() {
    FrameRateAnalyzer analyzer = new FrameRateAnalyzer(sourceIFrameVariant.format);
    assertThat(analyzer.analyzeFrameRate(createMockPlaylist(Collections.emptyList(), Uri.EMPTY))).isEqualTo(Format.NO_VALUE);
  }

  @Test
  public void test_analyzeFrameRate() {
    FrameRateAnalyzer analyzer = new FrameRateAnalyzer(sourceIFrameVariant.format);

    assertThat(analyzer.analyzeFrameRate(createMockPlaylist(test2SecondSegments, Uri.EMPTY))).isEqualTo(0.5f);

    final HlsMediaPlaylist playlist = createMockPlaylist(test3SecondSegments, Uri.EMPTY);
    assertThat(analyzer.analyzeFrameRate(playlist)).isWithin(0.001f).of(0.333f);
  }

  /**
   * Case 1 - frame rate for a playlist that has seen one call to
   * the {@link FrameRateAnalyzer#playlistUpdated(HlsMasterPlaylist, HlsMediaPlaylist)} method
   */
  @Test
  public void test_getFrameRateFor_case1() {
    assertThat(iframeOnlyVaraints.size()).isGreaterThan(0);   // base test assumptions

    final HlsMasterPlaylist.Variant variant = iframeOnlyVaraints.get(iframeOnlyVaraints.size() - 1);
    Format updatedFormat = variant.format
        .buildUpon()
        .setSampleMimeType("ignored for match")
        .build();

    FrameRateAnalyzer analyzer = new FrameRateAnalyzer(sourceIFrameVariant.format);
    analyzer.playlistUpdated(testMasterPlaylist, createMockPlaylist(test2SecondSegments, variant.url));

    // Frame rate should match measured value
    assertThat(analyzer.getFrameRateFor(updatedFormat)).isEqualTo(0.5f);
  }

  /**
   * Case 2 - frame rate derived from another updated playlists updated frame rate.  The original
   * {@link Format#frameRate} values for the updated playlist and the requested value are both known.
   */
  @Test
  public void test_getFrameRateFor_case2() {
    FrameRateAnalyzer analyzer = new FrameRateAnalyzer(sourceIFrameVariant.format);

    // Assert input conditions
    assertThat(iframeOnlyVaraints.size()).isGreaterThan(2);   // base test assumptions

    // the first curated variant is 1/2 the source frame rate
    final HlsMasterPlaylist.Variant curatedVariant1 = iframeOnlyVaraints.get(1);
    assertThat(curatedVariant1.format.frameRate).isEqualTo(0.5f);

    // the second curated variant is 1/3 the source frame rate
    final HlsMasterPlaylist.Variant curatedVariant2 = iframeOnlyVaraints.get(2);
    assertThat(curatedVariant2.format.frameRate).isWithin(0.0001f).of(0.3333f);

    analyzer.playlistUpdated(testMasterPlaylist, createMockPlaylist(test2SecondSegments, curatedVariant1.url));

    // two second segments * the ration of nonUpdated to updated variant is expected value
    float expected = 0.5f * curatedVariant2.format.frameRate / curatedVariant1.format.frameRate;
    assertThat(analyzer.getFrameRateFor(curatedVariant2.format)).isWithin(0.0001f).of(expected);

    // Update where the source playlist is first updated
    analyzer = new FrameRateAnalyzer(sourceIFrameVariant.format);
    analyzer.playlistUpdated(testMasterPlaylist, createMockPlaylist(test2SecondSegments, sourceIFrameVariant.url));

    // curated2 is .333f * source, so if source is 0.5 curated2 is 6 (0.16666)
    assertThat(analyzer.getFrameRateFor(curatedVariant2.format)).isWithin(0.0001f).of(0.1666f);

  }


  /**
   * Case 3 - frame rate when there have been no calls to
   * the {@link FrameRateAnalyzer#playlistUpdated(HlsMasterPlaylist, HlsMediaPlaylist)} method
   */
  @Test
  public void test_getFrameRateFor_case3() {
    FrameRateAnalyzer analyzer = new FrameRateAnalyzer(sourceIFrameVariant.format);
    assertThat(analyzer.getFrameRateFor(new Format.Builder().build())).isEqualTo(Format.NO_VALUE);

    // no playlist updates and source format has no frameRate, yields NO_VALUE

    Format noFrameRate = new Format.Builder()
        .setRoleFlags(C.ROLE_FLAG_TRICK_PLAY)
        .setHeight(720)
        .setWidth(1280)
        .setPeakBitrate(1373400)
        .build();
    assertThat(analyzer.getFrameRateFor(noFrameRate)).isEqualTo(Format.NO_VALUE);

    // no playlist updates and source format has a frameRate, that value is returned
    Format sourceWithFrameRate = sourceIFrameVariant.format.buildUpon()
        .setFrameRate(1.0f)
        .build();

    analyzer = new FrameRateAnalyzer(sourceWithFrameRate);

    assertThat(analyzer.getFrameRateFor(sourceWithFrameRate)).isEqualTo(1.0f);

    // curated frame rate should be the it's fraction of the source frame rate
    sourceWithFrameRate = sourceWithFrameRate.buildUpon()
        .setFrameRate(2.0f)
        .build();
    HlsMasterPlaylist.Variant lastVariant = iframeOnlyVaraints.get(iframeOnlyVaraints.size() - 1);
    float expectedFrameRate = 2.0f * lastVariant.format.frameRate;
    analyzer = new FrameRateAnalyzer(sourceWithFrameRate);

    assertThat(analyzer.getFrameRateFor(lastVariant.format)).isWithin(0.0001f).of(expectedFrameRate);

    analyzer = new FrameRateAnalyzer(null);
    assertThat(analyzer.getFrameRateFor(noFrameRate)).isEqualTo(Format.NO_VALUE);

  }

  @Test
  public void test_getFrameRateFor_noUpdate() {
    FrameRateAnalyzer analyzer = new FrameRateAnalyzer(sourceIFrameVariant.format);

    Uri uri = Uri.parse(SRC_IFRAME_URI);
    analyzer.playlistUpdated(testMasterPlaylist, createMockPlaylist(test2SecondSegments, uri));


    assertThat(analyzer.getFrameRateFor(baseFormat)).isEqualTo(0.5f);

    // since playlist was never loaded for the second format, it should be derived based on the multiple
    // second format's frame rate is 1/2 the base so should be 0.25
    //
  }

  @Test
  public void test_findIFrameOnlySourceFormat() {
    assertThat(FrameRateAnalyzer.findIFrameOnlySourceFormat(testMasterPlaylist)).isEqualTo(sourceIFrameVariant.format);
    assertThat(FrameRateAnalyzer.findIFrameOnlySourceFormat(HlsMasterPlaylist.EMPTY)).isNull();
  }
}
